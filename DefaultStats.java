/**
 * Holds static final variables for default statistics which can be used
 * to easily reconfigure stats without having to go through a bunch of
 * copy-pasted code and changing five hundred numbers to increase the
 * max health by 1.0.
 * 
 * 
 * 
 * @author Nelson Gomez
 */
public class DefaultStats
{
    public static final double maxHealth = 100.0;
    public static final double health = 100.0;
    public static final int stamina = 0;
    public static final int dexterity = 0;
    public static final int defense = 0;
}
